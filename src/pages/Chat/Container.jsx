import React from 'react';
import ChatUI from './UI';

class ChatPageContainer extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      messages: JSON.parse(localStorage.getItem('messages')) || [],
      message: '',
      intervalId: undefined
    };
    this.handleChange = this.handleChange.bind(this);
    this.onEnter = this.onEnter.bind(this);
    this.onKeyPress = this.onKeyPress.bind(this);

  }
  componentDidMount() {
    const intervalId = setInterval(() => {
      this.onEnter(true)();
    }, 15000);
    this.setState({ intervalId });
  }
  componentWillUnmount() {
    clearInterval(this.state.intervalId);
  }
  handleChange = (name) => (event) => {
    this.setState({
      [name]: event.target.value
    })
  }
  onEnter = (autoGenerated) => () => {
    if (this.state.message || autoGenerated) {
      let messages = [...this.state.messages];
      messages.push({
        autoGenerated,
        message: autoGenerated ?
          'This is an auto generated message' : this.state.message
      });
      this.setState({
        messages,
        message: autoGenerated ? this.state.message : ''
      });
      localStorage.setItem('messages', JSON.stringify(messages));
    }
  }
  onKeyPress(ev) {
    if (ev.key === 'Enter') {
      this.onEnter(false)();
    }
  }
  render() {
    console.log(this.state);
    return (
      <ChatUI
        {...this.state}
        handleChange={this.handleChange}
        onEnter={this.onEnter(false)}
        onKeyPress={this.onKeyPress}
      />
    )
  }
}
export default ChatPageContainer;